# Type variables in Python are used to create generic classes and functions.

x = 'Tommy'  # Type variable as a string
y = 25  # Type variable as an integer
z = 3.14  # Type variable as a float

print(type(x))  # Output: <class 'str'>
print(type(y))  # Output: <class 'int'>
print(type(z))  # Output: <class 'float'>   

typeof_x = type(x)  # Get the type of x
typeof_y = type(y)  # Get the type of y
typeof_z = type(z)  # Get the type of z

print(typeof_x)  # Output: <class 'str'>
print(typeof_y)  # Output: <class 'int'>

